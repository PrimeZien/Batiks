function loadProducts(type) {

    var products = getRelevantProducts(type);

    for (var i = 0; i < products.length; i++) {

        var product = products[i];
        var id = product.id;
        var name = product.name;
        var price = product.price;
        var image = product.image;

        var singleCard = document.createElement("div");
        singleCard.classList.add("single-card");

        if (i < 4) {
            var newMark = document.createElement("img");
            newMark.setAttribute("src", "images/new.png");
            newMark.classList.add("new-logo");
            singleCard.appendChild(newMark);
        }

        var singleCardImg = document.createElement("div");
        singleCardImg.classList.add("single-card-img");

        var img = document.createElement("img");
        img.setAttribute("src", image);

        var singleCardText = document.createElement("div");
        singleCardText.classList.add("single-card-text");

        var nameElement = document.createElement("p");
        nameElement.innerText = name;

        var priceElement = document.createElement("p");
        priceElement.innerText = "Rs. " + price + ".00";

        var viewMore = document.createElement("div");
        viewMore.classList.add("view-more");
        viewMore.innerText = "View More";
        viewMore.id = id;
        viewMore.setAttribute("onClick", "viewSingleProduct(this," + type + ")");

        singleCardText.appendChild(nameElement);
        singleCardText.appendChild(priceElement);
        singleCardText.appendChild(viewMore);

        singleCardImg.appendChild(img);

        singleCard.appendChild(singleCardImg);
        singleCard.appendChild(singleCardText);

        document.getElementById("cards-div").appendChild(singleCard);
    }
}

